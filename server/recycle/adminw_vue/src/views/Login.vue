<template>
  <div>
    <form class="login" @submit.prevent="login">
      <h1>Sign in</h1>
      <label>Email</label>
      <input required v-model="email" type="text" placeholder="Name" autocomplete="on" />
      <label>Password</label>
      <input required v-model="password" type="password" placeholder="Password" autocomplete="on" />
      <hr />
      <button type="submit">Login</button>
    </form>
  </div>
</template>
<script>
export default {
  name: "Login",
  data() {
    return {
      email: "",
      password: ""
    };
  },
  methods: {
    login: function() {
      this.$store
        .dispatch({ type: "login", email: this.email, password: this.password })
        .then(() => this.$router.push("/panel"));
    }
  }
};
</script>